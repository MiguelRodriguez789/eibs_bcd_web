@SuppressWarnings("serial")
public class JSEWD0047 extends JSEIBSServlet {

	
	protected void processRequest(ESS0030DSMessage user,
			HttpServletRequest req, HttpServletResponse res,
			HttpSession session, int screen) throws ServletException,
			IOException {

		main(user, req, res, session);
	}

	private void main(ESS0030DSMessage user, HttpServletRequest req,
			HttpServletResponse res, HttpSession session) throws IOException, ServletException {
		
		String pageName = "";
		MessageProcessor mp = null;
		String flagTypeConvenio = Util.getReqParameter("FLAG", req);
		
		try {
			mp = getMessageProcessor("EWD0047", req);
			EWD0047DSMessage msgHelp = (EWD0047DSMessage) mp.getMessageRecord("EWD0047DS");
	    	msgHelp.setEWDBNK( Util.getReqParameter("BANK", req) );
	    	msgHelp.setEWDFRQ( flagTypeConvenio.equalsIgnoreCase("TRUE") ? "X" : "");
			
			mp.sendMessage(msgHelp);
			
			JBObjList list = mp.receiveMessageRecordList("EWDOPE");
			
			if (mp.hasError(list)) {
				ELEERRMessage msgError = (ELEERRMessage) mp.getError(list);
				session.setAttribute("error", msgError);
				pageName = "error_viewer.jsp";
			} else {
				session.setAttribute("ewd0047Help", list);
				pageName = "EWD0047_commision_table_helpmessage.jsp";
			}
			
			forward(pageName, req, res);
		} finally {
			if (mp != null) mp.close();
		}
	}

}
