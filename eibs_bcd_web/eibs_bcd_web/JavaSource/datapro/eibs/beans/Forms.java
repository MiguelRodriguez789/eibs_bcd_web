package datapro.eibs.beans;

public class Forms {
	
	public String apfpro;  // product
	public String apffty;  // form type
	public String apffcd;  // form code
	public String apfdsc;  // form description
	public String apfpth;  // form path or form name
	public int    recnum;  // record number
	
	
	
	public String getApfdsc() {
		return apfdsc;
	}
	public void setApfdsc(String apfdsc) {
		this.apfdsc = apfdsc;
	}
	public String getApffcd() {
		return apffcd;
	}
	public void setApffcd(String apffcd) {
		this.apffcd = apffcd;
	}
	public String getApffty() {
		return apffty;
	}
	public void setApffty(String apffty) {
		this.apffty = apffty;
	}
	public String getApfpro() {
		return apfpro;
	}
	public void setApfpro(String apfpro) {
		this.apfpro = apfpro;
	}
	public String getApfpth() {
		return apfpth;
	}
	public void setApfpth(String apfpth) {
		this.apfpth = apfpth;
	}
	public int getRecnum() {
		return recnum;
	}
	public void setRecnum(int recnum) {
		this.recnum = recnum;
	}
	
	

}
